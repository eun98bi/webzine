function doDisplay1(){
	var con = document.getElementById("c1");
    var tit = document.getElementById("t1");
	if(con.style.display=='none'){
		con.style.display = 'block';
        tit.style.backgroundColor = 'white';
        tit.style.color = 'black';
	}else{
		con.style.display = 'none';
        tit.style.backgroundColor = '';
        tit.style.color = 'white';
	}
}

